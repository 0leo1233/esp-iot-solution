static void screen_clear(int color)
{
    lcd_info_t info;
    lcd.get_info(&info);

    uint16_t *buffer = malloc(info.width * sizeof(uint16_t));
    if (NULL == buffer) {
        for (size_t y = 0; y < info.height; y++) {
            for (size_t x = 0; x < info.width; x++) {
                lcd.draw_pixel(x, y, color);
            }
        }
    } else {
        for (size_t i = 0; i < info.width; i++) {
            buffer[i] = color;
        }

        for (int y = 0; y < info.height; y++) {
            lcd.draw_bitmap(0, y, info.width, 1, buffer);
        }

        free(buffer);
    }

}

static void InitCLUT(uint16_t *clut)
{
    uint32_t i = 0x00;
    uint16_t red = 0, green = 0, blue = 0;

    for (i = 0; i < ITERATION; i++) {
        red = (i * 8 * 256 / ITERATION) % 256;
        green = (i * 6 * 256 / ITERATION) % 256;
        blue = (i * 4 * 256 / ITERATION) % 256;
        red = red >> 3;
        red = red << 11;
        green = green >> 2;
        green = green << 5;
        blue = blue >> 3;
        clut[i] = red + green + blue;
    }
}

static void GenerateJulia_fpu(uint16_t size_x, uint16_t size_y, uint16_t offset_x, uint16_t offset_y, int32_t real, int32_t image, uint16_t *out_buffer)
{
    uint8_t i;
    uint16_t x, y;
    float tmp1, tmp2;
    float num_real, num_img;
    float radius;
    uint16_t zoom = 150;
    float c_real = (float)real / 1000.0f;
    float c_image = (float)image / 1000.0f;
    ESP_LOGI(TAG, "c = %f + %fi", c_real, c_image);

    for (y = 0; y < size_y; y++) {
        for (x = 0; x < size_x; x++) {
            num_real = y - offset_y;
            num_real = num_real / zoom;
            num_img = x - offset_x;
            num_img = num_img / zoom;
            i = 0;

            radius = 0;

            while ((i < ITERATION - 1) && (radius < 4)) {
                tmp1 = num_real * num_real;
                tmp2 = num_img * num_img;
                num_img = 2 * num_real * num_img + c_image;
                num_real = tmp1 - tmp2 + c_real;
                radius = tmp1 + tmp2;
                i++;
            }
            if (NULL != out_buffer) {
                out_buffer[y * size_x + x] = color_table[i];
            } else {
                lcd.draw_pixel(x, y, color_table[i]);
            }
        }
    }

    if (NULL != out_buffer) {
        lcd.draw_bitmap(0, 0, lcd_info.width, lcd_info.height, out_buffer);
    }
}

static void lcd_julia_test(uint16_t *pixels)
{
    int i;
    for (i = -900; i < -650; i += 2) {
        GenerateJulia_fpu(lcd_info.width, lcd_info.height, lcd_info.width / 2, lcd_info.height / 2, 0, i, pixels);
    }
    vTaskDelay(1000 / portTICK_PERIOD_MS);
}

static void color_hsv_to_rgb(uint16_t h, uint8_t s, uint8_t v, uint8_t *r, uint8_t *g, uint8_t *b)
{
    h = (uint32_t)((uint32_t)h * 255) / 360;
    s = (uint16_t)((uint16_t)s * 255) / 100;
    v = (uint16_t)((uint16_t)v * 255) / 100;

    uint8_t rr, gg, bb;

    uint8_t region, remainder, p, q, t;

    if (s == 0) {
        rr = v;
        gg = v;
        bb = v;
        *r = *g = *b = v;
        return ;
    }

    region = h / 43;
    remainder = (h - (region * 43)) * 6;

    p = (v * (255 - s)) >> 8;
    q = (v * (255 - ((s * remainder) >> 8))) >> 8;
    t = (v * (255 - ((s * (255 - remainder)) >> 8))) >> 8;

    switch (region) {
    case 0:
        rr = v;
        gg = t;
        bb = p;
        break;
    case 1:
        rr = q;
        gg = v;
        bb = p;
        break;
    case 2:
        rr = p;
        gg = v;
        bb = t;
        break;
    case 3:
        rr = p;
        gg = q;
        bb = v;
        break;
    case 4:
        rr = t;
        gg = p;
        bb = v;
        break;
    default:
        rr = v;
        gg = p;
        bb = q;
        break;
    }

    *r = rr;
    *g = gg;
    *b = bb;
}

static uint16_t rgb888_to_565(uint8_t r, uint8_t g, uint8_t b)
{
    return ((r & 0xF8) << 8) | ((g & 0xFC) << 3) | (b >> 3);
}

static void lcd_color_fade_test(uint16_t *pixels)
{
    uint8_t r, g, b;
    uint16_t w = lcd_info.width;
    uint16_t h = lcd_info.height;

    for (size_t i = 0; i < h; i++) {
        uint16_t color = rgb888_to_565(i * 256 / h, 0, 0);
        for (size_t j = 0; j < w; j++) {
            if (NULL == pixels) {
                lcd.draw_pixel(j, i, color);
            } else {
                pixels[i * w + j] = color;
            }
        }
    }
    if (NULL != pixels) {
        lcd.draw_bitmap(0, 0, w, h, pixels);
    }
    vTaskDelay(5000 / portTICK_PERIOD_MS);

    for (size_t i = 0; i < h; i++) {
        uint16_t color = rgb888_to_565(0, i * 256 / h, 0);
        for (size_t j = 0; j < w; j++) {
            if (NULL == pixels) {
                lcd.draw_pixel(j, i, color);
            } else {
                pixels[i * w + j] = color;
            }
        }
    }
    if (NULL != pixels) {
        lcd.draw_bitmap(0, 0, w, h, pixels);
    }
    vTaskDelay(5000 / portTICK_PERIOD_MS);

    for (size_t i = 0; i < h; i++) {
        uint16_t color = rgb888_to_565(0, 0, i * 256 / h);
        for (size_t j = 0; j < w; j++) {
            if (NULL == pixels) {
                lcd.draw_pixel(j, i, color);
            } else {
                pixels[i * w + j] = color;
            }
        }
    }
    if (NULL != pixels) {
        lcd.draw_bitmap(0, 0, w, h, pixels);
    }
    vTaskDelay(5000 / portTICK_PERIOD_MS);

    for (size_t i = 0; i < h; i++) {
        color_hsv_to_rgb(i * 360 / h, 100, 100, &r, &g, &b);
        uint16_t color = rgb888_to_565(r, g, b);
        for (size_t j = 0; j < w; j++) {
            if (NULL == pixels) {
                lcd.draw_pixel(j, i, color);
            } else {
                pixels[i * w + j] = color;
            }
        }
    }
    if (NULL != pixels) {
        lcd.draw_bitmap(0, 0, w, h, pixels);
    }
    vTaskDelay(5000 / portTICK_PERIOD_MS);

    for (size_t i = 0; i < h; i++) {
        color_hsv_to_rgb(360 - (i * 360 / h), 100, 100, &r, &g, &b);
        uint16_t color = rgb888_to_565(r, g, b);
        for (size_t j = 0; j < w; j++) {
            if (NULL == pixels) {
                lcd.draw_pixel(j, i, color);
            } else {
                pixels[i * w + j] = color;
            }
        }
    }
    if (NULL != pixels) {
        lcd.draw_bitmap(0, 0, w, h, pixels);
    }
    vTaskDelay(5000 / portTICK_PERIOD_MS);
}

static void lcd_color_test(uint16_t *pixels)
{
    const uint16_t color_table[] = {COLOR_RED, COLOR_GREEN, COLOR_BLUE, COLOR_YELLOW};

    if (NULL == pixels) {
        for (int c = 0; c < sizeof(color_table) / sizeof(uint16_t); c++) {
            for (size_t j = 0; j < lcd_info.height; j++) {
                for (size_t i = 0; i < lcd_info.width; i++) {
                    lcd.draw_pixel(i, j, color_table[c]);
                }
            }
            vTaskDelay(1000 / portTICK_PERIOD_MS);
        }
    } else {

        for (int c = 0; c < sizeof(color_table) / sizeof(uint16_t); c++) {
            for (size_t i = 0; i < lcd_info.width * lcd_info.height; i++) {
                pixels[i] = color_table[c];
            }
            lcd.draw_bitmap(0, 0, lcd_info.width, lcd_info.height, pixels);
            vTaskDelay(1000 / portTICK_PERIOD_MS);
        }
    }
}

